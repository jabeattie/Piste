import UIKit

extension UIColor {
    static var pisteRed: UIColor {
        return UIColor(red: 251/255.0, green: 51/255.0, blue: 83/255.0, alpha: 1.0)
    }
    
    static var pisteBlue: UIColor {
        return UIColor(red: 73/255.0, green: 83/255.0, blue: 255/255.0, alpha: 1.0)
    }
}
